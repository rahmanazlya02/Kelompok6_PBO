    private void kirimButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kirimButtonActionPerformed
        // TODO add your handling code here:
        blokC blokC = new blokC();
        blokC.setIdPerusahaan(Integer.parseInt(idPerusahaan_txt.getText()));
        blokC.setIncomeSblmCovid((String)Kondisi.getSelectedItem());
        blokC.setPenurunan((String) Penurunan.getSelectedItem());
        blokC.setPenurunanLain(PenurunanLanjutan.getText());
        blokC.setPeningkatan((String) Peningkatan.getSelectedItem());
        blokC.setPeningkatanLain(PeningkatanLanjutan.getText());
        blokC.setUpayaPeningkatan((String) Upaya.getSelectedItem());

        try {
            Database.getInstance().updateBlokC(blokC);
            JOptionPane.showMessageDialog(this, "Data Berhasil Diedit");
            contentScrollPane.setViewportView(new viewPanel(contentScrollPane, idPerusahaan));
        } catch (SQLException ex) {
            System.err.println(ex);
            JOptionPane.showMessageDialog(this, "Lengkapi Semua Data", "Data Gagal Tersimpan", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_kirimButtonActionPerformed

    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        // TODO add your handling code here:
        // Konfirmasi penghapusan data
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this record?", "Confirm Delete", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                // Panggil method deleteBlokA dari kelas Database
                Database.getInstance().deleteBlokC(idPerusahaan);
                JOptionPane.showMessageDialog(this, "Data successfully deleted.");
                clearForm();
            } catch (SQLException ex) {
                Logger.getLogger(editDataBlokA.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Failed to delete data: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_hapusActionPerformed
    

