    public void insertInfo() throws SQLException {
        String sql = "SELECT * FROM bloka WHERE id_perusahaan = ?";
        String sql2 = "SELECT * FROM blokc WHERE id_perusahaan = ?";

        try (Connection conn = Database.getInstance().getConnection();
             PreparedStatement pst = conn.prepareStatement(sql);
             PreparedStatement pst2 = conn.prepareStatement(sql2)) {

            pst.setInt(1, idPerusahaan);
            pst2.setInt(1, idPerusahaan);

            try (ResultSet rs = pst.executeQuery();
                 ResultSet rs2 = pst2.executeQuery()) {
                
                if (rs2.next()) {
                    idPerusahaan_txt.setText(rs2.getString("id_perusahaan"));
                    Kondisi.setSelectedItem(rs2.getString("income_Sblm_Covid"));
                    Penurunan.setSelectedItem(rs2.getString("penurunan"));
                    PenurunanLanjutan.setText(rs2.getString("penurunan_lain"));
                    Peningkatan.setSelectedItem(rs2.getString("peningkatan"));
                    PeningkatanLanjutan.setText(rs2.getString("peningkatan_lain"));
                    Upaya.setSelectedItem(rs2.getString("upaya_peningkatan"));
                }
            }
        } catch (SQLException e) {
            throw e;
        }
    }
